function [Z] = R_colormap(type_flag)
  % colormap()
  % 
  % Simple function to generate a color maps to display risk-map information.
  %
  % Written by Ryan Schultz.
  %
  
  % Define the colormaps.
  if(strcmpi(type_flag,'red-light'))
      n=50;
      Z=[225     190   190;  ...
         236     160   160;  ...
         232     135   135;  ...
         255     137   137;  ...
         255     105   105;  ...
         255     85    85 ;  ...
         255     77    77 ;  ...
         255     44    44 ;  ...
         255     0     0  ;  ...
         222     0     0  ;  ...
         210     0     0  ;  ...
         184     0     0  ;  ...
         154     0     0  ;  ...
         126     0     0  ;  ...
         104     0     0  ;  ...
         80      0     0  ;  ...
         58      0     0  ;  ...
         45      0     0  ];
     Z=flipud(Z);
  elseif(strcmpi(type_flag,'population'))
      n=8*4;
      Z=[61      38    157;  ...
         90      60    210;  ...
         222     196   172;  ...
         219     190   134;  ... 
         228     170   56 ;  ...
         250     250   20 ;  ...
         224     224   224;  ...
         255     255   255];
  elseif(strcmpi(type_flag,'depth'))
      n=8;
      Z=[215     186   140;  ...
         131     111   93 ;  ...
         95      81    66 ;  ...
         80      68    57 ];
  elseif(strcmpi(type_flag,'Vs30'))
      n=8*2;
      Z=[198     33    39 ;  ...
         245     144   30 ;  ...
         254     201   9  ;  ...
         245     236   21 ;  ...
         145     202   131;  ...
         110     193   104;  ...
         50      153   71 ;  ...
         43      87    87 ];
  elseif(strcmpi(type_flag,'nuisance'))
      n=5*2;
      Z=[181    181    250;  ...
         72      83    250;  ...
         0       33    250;  ...
         0       18    165;  ...
         0       3     67 ];
  elseif(strcmpi(type_flag,'damage'))
      n=5*2;
      Z=[255    205    87 ;  ...
         255    139    98 ;  ...
         255     89    22 ;  ...
         231     76    12 ;  ...
         192     56    0  ];
  elseif(strcmpi(type_flag,'LPR'))
      n=5*2;
      Z=[144    159    95 ;  ...
         111    138    75 ;  ...
         79     116    54 ;  ...
         45      95    34 ;  ...
         13      73    13 ];
  elseif(strcmpi(type_flag,'indicies'))
      n=5*2;
      Z=[72      83    250 ;  ...
         255    205    87 ;  ...
         144    159    95 ];
  else
      n=50;
      Z=0;
  end
  
  % Make 16-bit RGB values between 0-1.
  Z=Z/255;
  
  % Interpolate to the desired number of samples.
  I=1:length(Z);
  Z=interp1(I,Z,min(I):1/n:max(I));
   
return;